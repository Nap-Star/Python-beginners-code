#Find out if it's a prime number
x=int(input("Enter a number"))

if x%2 == 0:
  print("This is not a prime number") 
elif x%3 == 0:
  print("This is not a prime number") 
elif x%5 == 0:
  print("This is not a prime number") 
elif x%7 == 0:
  print("This is not a prime number") 
else:
  print("This is a prime number") 


